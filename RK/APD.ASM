**
** IN
**   A2-A3 SCRATCH
**   A8 OBJ
**
** JFL 10 JAN 93
**
CLRGCOL
	;GET 1ST OBJ
	MOVL	@GCOLLIST,A3
	JRZ	CGCXBAD

	CMP	A3,A8
	JRNE	CGCLOOP

	;UNLINK FIRST
	MOVL	*A8(OGCOLLINK),A2
	MOVL	A2,@GCOLLIST
	CLR	A14
	MOVL	A14,*A8(OGCOL)
	JRUC	CGCX

