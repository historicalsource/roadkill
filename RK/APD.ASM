	;GET NEXT
	MOVE	A3,A2			;SAVE PREV
	MOVL	*A3(OGCOLLINK),A3
	JRZ	CGCXBAD

CGCCHECK
	;IS THIS THE OBJ WE WANT
	CMP	A3,A8
	JRNE	CGCLOOP
	
	;UNLINK
	MOVL	*A3(OGCOLLINK),*A2(OGCOLLINK)
	CLR	A14
	MOVL	A14,*A8(OGCOL)
	
CGCX
	EXGPC	B8
CGCXBAD
	TLOCKUP
	EXGPC	B8

**
** GCOLWATCHER -- PROCESS
**
** JFL 01 OCT 92
** JFL 30 OCT 92; NO OFLAGS BIT
** JFL 10 JAN 93; GCOLLLIST
**
GCOLWATCHER
	;GET 1ST OBJ TO CHECK FOR GROUND COLLISION
	MOVL	@GCOLLIST,A8
	JRZ	GCWX

	;ZERO Q COUNT
	CLR	A1		;QCOUNT
	MOVI	J64Q,A9	 	;TOP OF Q

GCWLOOP
	;HAS IT HIT?	
	MOVL	*A8(OYACC),A14	
	JRNZ	GCWNEXT

	;Q IT UP
	INC	A1		;QCOUNT++
	MOVL	*A8(OGCOL),A14
	MOVL	A14,*A9+
	MOVL	A8,*A9+
	

GCWNEXT
	;NEXT OBJ
	MOVL	*A8(OGCOLLINK),A8
	JRNZ	GCWLOOP

	;ANY TO DO?
	MOVE	A1,A1
	JRZ	GCWX

	;TERM THE Q
	CLR	A14
	MOVL	A14,*A9

	;RESET TO TOP OF Q
	MOVI	J64Q,A9		;TOP OF Q
	JRUC	GCWPNEXT

